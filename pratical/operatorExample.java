class operatorExample{
public static void main(String args[]){
int a=18, b=24;
System.out.println("a+b=" +(a+b));
System.out.println("a*b=" +(a*b));
System.out.println(a == b);
System.out.println(a > b);
System.out.println(a <= b);
System.out.println(a/b);
System.out.println((18>24)&&(18<24));
System.out.println((18>24)||(18<24));
}
}
